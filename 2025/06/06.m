## 理想的な筋肉バランスの指針

美しい筋肉バランスは以下の比率に基づいています：

1. **黄金比に基づく体型**:
   - 肩幅：ウエスト = 1.618：1（黄金比）
   - 胸囲：ウエスト = 1.4：1
   - 上腕：前腕 = 1.5：1
   - 大腿：ふくらはぎ = 1.75：1

2. **筋肉群間のバランス**:
   - 胸部：背中 = 1：1（均等な発達）
   - 大腿四頭筋：ハムストリング = 1：1（均等な発達）
   - プッシュ系：プル系 = 1：1（力のバランス）

3. **見た目の調和**:
   - 上半身と下半身の均衡
   - V字型上半身（広い肩、引き締まったウエスト）
   - 全体的な対称性

これらの比率に近づけるため、トレーニングセッションは継続的に再構成されます。

